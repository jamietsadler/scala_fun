   println(x)

   val aString: String = "Scala fun";
   println(aString)

   val aChar: Char = 'v';

   val sShort: Short = 1231;
   val aLong: Long = 321231331231L;
   println(aLong)
   val aFloat: Float = 3.0f;
   val aDouble: Double = 3.4235;



}
